#!/bin/sh

# ১. ব্যাকগ্রাউন্ডে frps সার্ভার চালু করা হচ্ছে
/frp/frps -c /frp/frps.toml &

# ২. sslh (ট্রাফিক পুলিশ) চালু করা হচ্ছে
# এটি রেলওয়ের দেওয়া পাবলিক পোর্টে ($PORT) বসে থাকবে।
# যদি রিকোয়েস্টটি SSH হয়, তবে 6000 পোর্টে (ইউজার) পাঠাবে।
# অন্য যেকোনো রিকোয়েস্ট (যেমন frpc কন্ট্রোল) 7000 পোর্টে (ওনার) পাঠাবে।
exec sslh -f -u root -p 0.0.0.0:${PORT:-8080} --ssh 127.0.0.1:6000 --anyprot 127.0.0.1:7000
